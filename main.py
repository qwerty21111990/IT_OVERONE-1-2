a = int(input("Введите первое число: "))
b = int(input("Введите второе число: "))
s = input("Введите операцию которую вы хотите сделать: ")
if s == "+":
    print(a+b)
elif s == "-":
    print(a-b)
elif s == "*":
    print(a*b)
elif s == "/":
    print(a/b)
elif s =="%":
    print(a%b)
elif s == "^":
    print("a**b")




